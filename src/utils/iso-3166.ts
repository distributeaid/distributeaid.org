export function getCoordsAlpha3(alpha3: string): Coordinate | undefined {
  const country = countries.find((element) => alpha3 === element.alpha3)
  if (country !== undefined) {
    const coord = coordinates[country.alpha2]
    return { lat: coord.lat, lon: coord.lng }
  } else {
    return undefined
  }
}
